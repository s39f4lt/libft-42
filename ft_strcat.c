#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	char	*n;

	n = s1;
	i = ft_strlen(s2);
	while (*n != '\0')
		n++;
	while (i > 0)
	{
		*n = *(char*)s2;
		n++;
		s2++;
		i--;
	}
	*n = '\0';
	return (s1);
}
