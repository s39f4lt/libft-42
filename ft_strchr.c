#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *n;

	n = (char*)s;
	while (*n != '\0')
	{
		if ((char)c == *n)
			return (n);
		n++;
	}
	if (c == '\0')
		return (n);
	return (NULL);
}
